package ArraydeVetores;

public class Computadoras {
    String marca;
    String caracteristicasprocesador;
    String sistemaoperativo;
    int cantidadmemoria;
    double precio;

    public Computadoras(String marca, String caracteristicasprocesador, String sistemaoperativo, int cantidadmemoria, double precio) {
        this.marca = marca;
        this.caracteristicasprocesador = caracteristicasprocesador;
        this.sistemaoperativo = sistemaoperativo;
        this.cantidadmemoria = cantidadmemoria;
        this.precio = precio;
    }
}
